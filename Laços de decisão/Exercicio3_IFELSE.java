import java.util.Scanner;

public class Exercicio3_IFELSE {

	public static void main(String[] args) {
		//Faça um programa que receba a idade de uma pessoa e mostre na saída em qual 
		//categoria ela se encontra:
		//10-14 infantil
		//15-17 juvenil
		//18-25 adulto
		
		Scanner L = new Scanner(System.in);
		int idade;
		String nome;
		
		System.out.println("Digite sua idade e o programa mostrará qual sua categoria: ");
		nome = L.nextLine();
		
		System.out.println("Digite a sua idade: ");
		idade = L.nextInt();
		
		if(idade >=10 && idade <=14) 
		{
			System.out.println("Olá " +nome+ " sua idade é " +idade+ " e você está na categoria infantil");
		}
		else if(idade >=15 && idade <=17) 
		{
			System.out.println("Olá " +nome+ " sua idade é " +idade+ " e você está na categoria juvenil");
		}
		else if(idade >=18 && idade <=25) 
		{
			System.out.println("Olá " +nome+ " sua idade é " +idade+ " e você está na categoria adulto");
		}
		else 
		{
			System.out.println("Olá " +nome+ " sua idade é " +idade+ " e sua idade não entra em nenhuma das nossas categorias");
			
		}
		
		
		


	}

}
